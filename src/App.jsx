function bandOf(classId) {
  if (!classId) return "low";
  if (classId.indexOf("keyaki") === 0) return "keyaki";
  const g = Number(classId.split("-")[0]);
  if (g <= 2) return "low";
  if (g <= 4) return "mid";
  return "high";
}
